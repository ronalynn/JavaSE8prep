//Explain an Object's Lifecycle (creation, "dereference by reassignment" and garbage collection)

public class b4ObjectLifecycle {
    /**
     * An object is an instance of a class.
     * Objects are created using keyword 'new' and a constructor:
     * Date d = new Date();
     * Constructor name's have to match the class and there is no return type.
     *
     * Objects are stored in a java program's memory heap.
     *
     * Garbage collection process:
     * When an object is no longer reachable, they are automatically deleted from the memory heap/.
     * An object becomes unreachable when there is no reference variable referring to it or the object is out of scope.
     * We cannot control when garbage collection  will happen.
     */

}
